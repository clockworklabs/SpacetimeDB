//! Schema migration candidates for the engine DST driver.
//!
//! This module deliberately generates only changes expected to be handled by
//! engine auto-migration. If a generated schema requires a manual migration
//! plan, the engine driver treats that as a harness bug.

use super::model::{ColumnDomain, Model};
use crate::rng::{choice, choose_index, Choice, WeightedChoice};
use crate::schema::{
    ColumnPlan, IndexAlgorithm, IndexPlan, SchemaGenerator, SchemaNames, SchemaPlan, SchemaProfile, SequencePlan,
    TablePlan, Type, UniqueConstraintPlan,
};
use spacetimedb_runtime::sim::Rng;

const MAX_SUM_VARIANTS: u8 = 32;
const MAX_EVENT_COLUMNS: usize = 32;
const MAX_TABLE_COLUMNS: usize = 32;
const MAX_TABLES: usize = 128;

/// A fully materialized target schema for one migration interaction.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct Migration {
    schema: SchemaPlan,
}

/// A deterministic schema edit used while building a migration candidate.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum SchemaRewrite {
    AddTable { table: TablePlan },
    RemoveTable { table: String },
    AlterTable { table: String, ops: Vec<TableMigrationOp> },
}

/// Table-local migration operations generated by the DST harness.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum TableMigrationOp {
    ChangeAccess,
    AddColumn {
        ty: Type,
    },
    AddIndex {
        columns: Vec<usize>,
        algorithm: IndexAlgorithm,
    },
    RemoveIndex {
        columns: Vec<usize>,
    },
    AddSequence {
        sequence: SequencePlan,
    },
    RemoveSequence {
        column: usize,
    },
    AddUniqueConstraint {
        columns: Vec<usize>,
    },
    RemoveUniqueConstraint {
        columns: Vec<usize>,
    },
    ChangePrimaryKey {
        column: Option<usize>,
    },
    ChangeIndex {
        columns: Vec<usize>,
    },
    ChangeColumnType {
        column: usize,
    },
    ReschemaEventTable,
}

/// Weighted categories of auto-migration surfaces to probe.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
enum MigrationChoice {
    AddTable,
    RemoveTable,
    AddColumn,
    AddIndex,
    RemoveIndex,
    ChangeIndex,
    AddSequence,
    RemoveSequence,
    AddUniqueConstraint,
    RemoveUniqueConstraint,
    DropPrimaryKeyAndWidenSum,
    WidenSumColumn,
    ReschemaEventTable,
}

impl WeightedChoice for MigrationChoice {
    const CHOICES: &'static [Choice<Self>] = &[
        choice(2, Self::AddTable),
        choice(1, Self::RemoveTable),
        choice(14, Self::AddColumn),
        choice(10, Self::AddIndex),
        choice(8, Self::RemoveIndex),
        choice(10, Self::ChangeIndex),
        choice(18, Self::AddSequence),
        choice(8, Self::RemoveSequence),
        choice(12, Self::AddUniqueConstraint),
        choice(8, Self::RemoveUniqueConstraint),
        choice(6, Self::DropPrimaryKeyAndWidenSum),
        choice(12, Self::WidenSumColumn),
        choice(8, Self::ReschemaEventTable),
    ];
}

impl Migration {
    pub(crate) fn from_schema(schema: SchemaPlan) -> Self {
        Self { schema }
    }

    #[cfg(test)]
    pub(crate) fn from_rewrites(base: &SchemaPlan, rewrites: Vec<SchemaRewrite>) -> anyhow::Result<Self> {
        let mut schema = base.clone();
        for rewrite in &rewrites {
            rewrite.apply_to(&mut schema)?;
        }
        Ok(Self::from_schema(schema))
    }

    pub(crate) fn schema(&self) -> &SchemaPlan {
        &self.schema
    }

    pub(crate) fn choose_rewrite(rng: &Rng, schema: &SchemaPlan, model: &Model) -> Option<SchemaRewrite> {
        for _ in 0..16 {
            let choice = MigrationChoice::pick(rng);
            if let Some(rewrite) = pick_rewrite(rng, Self::candidates_for(rng, schema, model, choice)) {
                return Some(rewrite);
            }
        }

        pick_rewrite(rng, Self::candidates(rng, schema, model))
    }

    pub(crate) fn candidates(rng: &Rng, schema: &SchemaPlan, model: &Model) -> Vec<SchemaRewrite> {
        <MigrationChoice as WeightedChoice>::CHOICES
            .iter()
            .flat_map(|choice| Self::candidates_for(rng, schema, model, choice.value()))
            .collect()
    }

    fn candidates_for(rng: &Rng, schema: &SchemaPlan, model: &Model, choice: MigrationChoice) -> Vec<SchemaRewrite> {
        match choice {
            MigrationChoice::AddTable => add_table_rewrites(rng, schema),
            MigrationChoice::RemoveTable => remove_table_rewrites(schema, model),
            MigrationChoice::AddColumn => add_column_rewrites(schema),
            MigrationChoice::AddIndex => add_index_rewrites(schema),
            MigrationChoice::RemoveIndex => remove_index_rewrites(schema),
            MigrationChoice::ChangeIndex => change_index_rewrites(schema),
            MigrationChoice::AddSequence => add_sequence_rewrites(schema, model),
            MigrationChoice::RemoveSequence => remove_sequence_rewrites(schema),
            MigrationChoice::AddUniqueConstraint => add_unique_constraint_rewrites(schema, model),
            MigrationChoice::RemoveUniqueConstraint => remove_unique_constraint_rewrites(schema),
            MigrationChoice::DropPrimaryKeyAndWidenSum => drop_primary_key_and_widen_sum_rewrites(schema),
            MigrationChoice::WidenSumColumn => widen_sum_column_rewrites(schema),
            MigrationChoice::ReschemaEventTable => reschema_event_table_rewrites(schema, model),
        }
    }
}

fn pick_rewrite(rng: &Rng, mut candidates: Vec<SchemaRewrite>) -> Option<SchemaRewrite> {
    let idx = choose_index(rng, candidates.len())?;
    Some(candidates.swap_remove(idx))
}

fn add_table_rewrites(rng: &Rng, schema: &SchemaPlan) -> Vec<SchemaRewrite> {
    if schema.tables.len() >= MAX_TABLES {
        return Vec::new();
    }

    let generator = SchemaGenerator::new(rng.clone(), SchemaProfile::engine_dst());
    [false, true]
        .into_iter()
        .map(|is_event| SchemaRewrite::AddTable {
            table: generator.gen_table_for_schema(schema, is_event),
        })
        .collect()
}

fn remove_table_rewrites(schema: &SchemaPlan, model: &Model) -> Vec<SchemaRewrite> {
    let non_event_tables = schema.tables.iter().filter(|table| !table.is_event).count();

    schema
        .tables
        .iter()
        .filter_map(|table| {
            let row_count = model.row_count_by_table_name(&table.name);
            let pristine = row_count == 0 && !model.ever_inserted_by_table_name(&table.name);
            ((table.is_event && row_count == 0) || (!table.is_event && pristine && non_event_tables > 1)).then(|| {
                SchemaRewrite::RemoveTable {
                    table: table.name.clone(),
                }
            })
        })
        .collect()
}

fn add_column_rewrites(schema: &SchemaPlan) -> Vec<SchemaRewrite> {
    let types = addable_column_types(schema);
    schema
        .tables
        .iter()
        .filter(|table| !table.is_event && table.columns.len() < MAX_TABLE_COLUMNS)
        .flat_map(|table| {
            types.iter().copied().map(|ty| {
                SchemaRewrite::alter_table(
                    table,
                    [TableMigrationOp::ChangeAccess, TableMigrationOp::AddColumn { ty }],
                )
            })
        })
        .collect()
}

fn addable_column_types(schema: &SchemaPlan) -> Vec<Type> {
    Type::ALL
        .iter()
        .copied()
        .filter(|ty| !matches!(ty, Type::Sum { .. }) || !schema_has_sum_column(schema))
        .collect()
}

fn schema_has_sum_column(schema: &SchemaPlan) -> bool {
    schema
        .tables
        .iter()
        .flat_map(|table| table.columns.iter())
        .any(|column| matches!(column.ty, Type::Sum { .. }))
}

fn add_index_rewrites(schema: &SchemaPlan) -> Vec<SchemaRewrite> {
    schema
        .tables
        .iter()
        .filter(|table| !table.is_event)
        .flat_map(|table| {
            addable_indexes(table).into_iter().map(|(columns, algorithm)| {
                SchemaRewrite::alter_table(table, [TableMigrationOp::AddIndex { columns, algorithm }])
            })
        })
        .collect()
}

fn remove_index_rewrites(schema: &SchemaPlan) -> Vec<SchemaRewrite> {
    schema
        .tables
        .iter()
        .filter(|table| !table.is_event)
        .flat_map(|table| {
            changeable_index_columns(table)
                .into_iter()
                .map(|columns| SchemaRewrite::alter_table(table, [TableMigrationOp::RemoveIndex { columns }]))
        })
        .collect()
}

fn change_index_rewrites(schema: &SchemaPlan) -> Vec<SchemaRewrite> {
    schema
        .tables
        .iter()
        .filter(|table| !table.is_event)
        .flat_map(|table| {
            changeable_index_columns(table).into_iter().map(|columns| {
                SchemaRewrite::alter_table(
                    table,
                    [
                        TableMigrationOp::ChangeAccess,
                        TableMigrationOp::ChangeIndex { columns },
                    ],
                )
            })
        })
        .collect()
}

fn add_sequence_rewrites(schema: &SchemaPlan, model: &Model) -> Vec<SchemaRewrite> {
    let mut rewrites = Vec::new();

    for table in schema.tables.iter().filter(|table| !table.is_event) {
        let row_count = model.row_count_by_table_name(&table.name);
        let pristine = row_count == 0 && !model.ever_inserted_by_table_name(&table.name);

        if pristine {
            rewrites.extend(
                addable_sequences(table)
                    .into_iter()
                    .map(|sequence| SchemaRewrite::alter_table(table, [TableMigrationOp::AddSequence { sequence }])),
            );
        }

        if row_count > 0 {
            rewrites.extend(
                addable_sequence_boundary_probes(table, |column| column_domain(model, table, column))
                    .into_iter()
                    .map(|sequence| SchemaRewrite::alter_table(table, [TableMigrationOp::AddSequence { sequence }])),
            );
        }
    }

    rewrites
}

fn remove_sequence_rewrites(schema: &SchemaPlan) -> Vec<SchemaRewrite> {
    schema
        .tables
        .iter()
        .filter(|table| !table.is_event)
        .flat_map(|table| {
            removable_sequence_columns(table)
                .into_iter()
                .map(|column| SchemaRewrite::alter_table(table, [TableMigrationOp::RemoveSequence { column }]))
        })
        .collect()
}

fn add_unique_constraint_rewrites(schema: &SchemaPlan, model: &Model) -> Vec<SchemaRewrite> {
    let mut rewrites = Vec::new();

    for table in schema.tables.iter().filter(|table| !table.is_event) {
        let row_count = model.row_count_by_table_name(&table.name);
        let pristine = row_count == 0 && !model.ever_inserted_by_table_name(&table.name);
        if !pristine {
            continue;
        }

        for columns in addable_unique_constraint_columns(table) {
            let mut ops = Vec::new();
            if !has_index(table, &columns) {
                ops.push(TableMigrationOp::AddIndex {
                    columns: columns.clone(),
                    algorithm: IndexAlgorithm::BTree,
                });
            }
            ops.push(TableMigrationOp::AddUniqueConstraint { columns });
            rewrites.push(SchemaRewrite::alter_table(table, ops));
        }
    }

    rewrites
}

fn remove_unique_constraint_rewrites(schema: &SchemaPlan) -> Vec<SchemaRewrite> {
    schema
        .tables
        .iter()
        .filter(|table| !table.is_event)
        .flat_map(|table| {
            removable_unique_constraint_columns(table).into_iter().map(|columns| {
                SchemaRewrite::alter_table(table, [TableMigrationOp::RemoveUniqueConstraint { columns }])
            })
        })
        .collect()
}

fn drop_primary_key_and_widen_sum_rewrites(schema: &SchemaPlan) -> Vec<SchemaRewrite> {
    schema
        .tables
        .iter()
        .filter(|table| !table.is_event && table.primary_key.is_some() && table.sequences.is_empty())
        .flat_map(|table| {
            widenable_sum_columns(table).into_iter().map(|column| {
                SchemaRewrite::alter_table(
                    table,
                    [
                        TableMigrationOp::ChangePrimaryKey { column: None },
                        TableMigrationOp::ChangeColumnType { column },
                    ],
                )
            })
        })
        .collect()
}

fn widen_sum_column_rewrites(schema: &SchemaPlan) -> Vec<SchemaRewrite> {
    schema
        .tables
        .iter()
        .filter(|table| !table.is_event)
        .flat_map(|table| {
            widenable_sum_columns(table).into_iter().map(|column| {
                SchemaRewrite::alter_table(
                    table,
                    [
                        TableMigrationOp::ChangeAccess,
                        TableMigrationOp::ChangeColumnType { column },
                    ],
                )
            })
        })
        .collect()
}

fn reschema_event_table_rewrites(schema: &SchemaPlan, model: &Model) -> Vec<SchemaRewrite> {
    schema
        .tables
        .iter()
        .filter(|table| table.is_event && model.row_count_by_table_name(&table.name) == 0)
        .filter(|table| table.columns.len() < MAX_EVENT_COLUMNS)
        .map(|table| {
            SchemaRewrite::alter_table(
                table,
                [TableMigrationOp::ChangeAccess, TableMigrationOp::ReschemaEventTable],
            )
        })
        .collect()
}

impl SchemaRewrite {
    fn alter_table(table: &TablePlan, ops: impl Into<Vec<TableMigrationOp>>) -> Self {
        Self::AlterTable {
            table: table.name.clone(),
            ops: ops.into(),
        }
    }

    pub(crate) fn apply_to(&self, schema: &mut SchemaPlan) -> anyhow::Result<()> {
        match self {
            Self::AddTable { table } => {
                schema.tables.push(table.clone());
            }
            Self::RemoveTable { table } => {
                let table = table_position(schema, table)?;
                schema.tables.remove(table);
            }
            Self::AlterTable { table, ops } => {
                let table = table_position(schema, table)?;
                let table_plan = &mut schema.tables[table];
                for op in ops {
                    apply_table_op(table_plan, op.clone())?;
                }
            }
        }
        Ok(())
    }
}

fn table_position(schema: &SchemaPlan, table: &str) -> anyhow::Result<usize> {
    schema
        .tables
        .iter()
        .position(|table_plan| table_plan.name == table)
        .ok_or_else(|| anyhow::anyhow!("migration references missing table {table}"))
}

fn apply_table_op(table: &mut TablePlan, op: TableMigrationOp) -> anyhow::Result<()> {
    match op {
        TableMigrationOp::ChangeAccess => {
            table.is_public = !table.is_public;
        }
        TableMigrationOp::AddColumn { ty } => {
            anyhow::ensure!(!table.is_event, "add-column migration selected event table");
            anyhow::ensure!(
                table.columns.len() < MAX_TABLE_COLUMNS,
                "table already has the configured maximum number of columns"
            );
            table.columns.push(ColumnPlan {
                name: SchemaNames::fresh_column_name(table, "added_col"),
                ty,
            });
        }
        TableMigrationOp::AddIndex { columns, algorithm } => {
            ensure_columns_exist(table, &columns)?;
            anyhow::ensure!(
                !has_index(table, &columns),
                "add-index migration selected existing index columns"
            );
            table.indexes.push(IndexPlan { columns, algorithm });
        }
        TableMigrationOp::RemoveIndex { columns } => {
            ensure_columns_exist(table, &columns)?;
            let Some(index) = table.indexes.iter().position(|index| index.columns == columns) else {
                anyhow::bail!("remove-index migration references missing index on columns {columns:?}");
            };
            anyhow::ensure!(
                !is_required_index(table, &columns),
                "remove-index migration selected a required index"
            );
            table.indexes.remove(index);
        }
        TableMigrationOp::AddSequence { sequence } => {
            let column = sequence.column;
            let Some(column_plan) = table.columns.get(column) else {
                anyhow::bail!("add-sequence migration references missing column {column}");
            };
            anyhow::ensure!(
                column_plan.ty.is_integral(),
                "add-sequence migration selected non-integral column"
            );
            anyhow::ensure!(
                table.sequences.iter().all(|existing| existing.column != column),
                "add-sequence migration selected an already sequenced column"
            );
            table.sequences.push(sequence);
        }
        TableMigrationOp::RemoveSequence { column } => {
            anyhow::ensure!(
                column < table.columns.len(),
                "remove-sequence migration references missing column"
            );
            let Some(sequence) = table.sequences.iter().position(|sequence| sequence.column == column) else {
                anyhow::bail!("remove-sequence migration references missing sequence on column {column}");
            };
            table.sequences.remove(sequence);
        }
        TableMigrationOp::AddUniqueConstraint { columns } => {
            ensure_columns_exist(table, &columns)?;
            anyhow::ensure!(
                !table
                    .unique_constraints
                    .iter()
                    .any(|constraint| constraint.columns == columns),
                "add-constraint migration selected existing constraint columns"
            );
            anyhow::ensure!(
                has_index(table, &columns),
                "add-constraint migration requires a matching index"
            );
            table.unique_constraints.push(UniqueConstraintPlan { columns });
        }
        TableMigrationOp::RemoveUniqueConstraint { columns } => {
            ensure_columns_exist(table, &columns)?;
            let Some(constraint) = table
                .unique_constraints
                .iter()
                .position(|constraint| constraint.columns == columns)
            else {
                anyhow::bail!("remove-constraint migration references missing constraint on columns {columns:?}");
            };
            anyhow::ensure!(
                !table.primary_key.is_some_and(|primary_key| columns == [primary_key]),
                "remove-constraint migration selected primary-key constraint"
            );
            table.unique_constraints.remove(constraint);
        }
        TableMigrationOp::ChangePrimaryKey { column } => {
            if let Some(column) = column {
                anyhow::ensure!(
                    column < table.columns.len(),
                    "primary-key migration references missing column"
                );
            }
            table.primary_key = column;
        }
        TableMigrationOp::ChangeIndex { columns } => {
            ensure_columns_exist(table, &columns)?;
            anyhow::ensure!(
                !is_required_index(table, &columns),
                "index migration selected a required index"
            );
            let Some(index_plan) = table.indexes.iter_mut().find(|index| index.columns == columns) else {
                anyhow::bail!("index migration references missing index on columns {columns:?}");
            };
            index_plan.algorithm = match index_plan.algorithm {
                IndexAlgorithm::BTree => IndexAlgorithm::Hash,
                IndexAlgorithm::Hash => IndexAlgorithm::BTree,
            };
        }
        TableMigrationOp::ChangeColumnType { column } => {
            widen_sum_column(table, Some(column))?;
        }
        TableMigrationOp::ReschemaEventTable => {
            anyhow::ensure!(table.is_event, "event-table migration selected non-event table");
            anyhow::ensure!(
                table.columns.len() < MAX_EVENT_COLUMNS,
                "event table already has the configured maximum number of columns"
            );
            table.columns.push(ColumnPlan {
                name: SchemaNames::fresh_column_name(table, "reschema_payload"),
                ty: Type::U64,
            });
        }
    }

    Ok(())
}

fn widenable_sum_columns(table: &TablePlan) -> Vec<usize> {
    table
        .columns
        .iter()
        .enumerate()
        .filter_map(|(column, column_plan)| match column_plan.ty {
            Type::Sum { variants } if variants < MAX_SUM_VARIANTS => Some(column),
            _ => None,
        })
        .collect()
}

fn addable_indexes(table: &TablePlan) -> Vec<(Vec<usize>, IndexAlgorithm)> {
    (0..table.columns.len())
        .filter_map(|column| {
            let columns = vec![column];
            (!has_index(table, &columns)).then_some((columns, IndexAlgorithm::BTree))
        })
        .collect()
}

fn changeable_index_columns(table: &TablePlan) -> Vec<Vec<usize>> {
    table
        .indexes
        .iter()
        .filter_map(|index| (!is_required_index(table, &index.columns)).then_some(index.columns.clone()))
        .collect()
}

fn addable_sequences(table: &TablePlan) -> Vec<SequencePlan> {
    table
        .columns
        .iter()
        .enumerate()
        .filter_map(|(column, column_plan)| {
            (column_plan.ty.is_integral() && table.sequences.iter().all(|sequence| sequence.column != column))
                .then(|| SequencePlan::new(column, column_plan.ty).expect("column type checked above"))
        })
        .collect()
}

fn addable_sequence_boundary_probes(
    table: &TablePlan,
    column_domain: impl Fn(usize) -> Option<ColumnDomain>,
) -> Vec<SequencePlan> {
    table
        .columns
        .iter()
        .enumerate()
        .filter_map(|(column, column_plan)| {
            let domain = column_domain(column)?;
            if column_plan.ty != Type::U64
                || table.sequences.iter().any(|sequence| sequence.column == column)
                || domain.sequenced
                || !domain.single_column_indexed
                || !domain.single_column_unique
            {
                return None;
            }

            domain
                .integral_values()
                .filter_map(|max_value| SequencePlan::with_existing_value_as_max(column, column_plan.ty, max_value))
                .find(|sequence| added_sequence_precheck_range_is_clear(&domain, sequence))
        })
        .collect()
}

fn added_sequence_precheck_range_is_clear(domain: &ColumnDomain, sequence: &SequencePlan) -> bool {
    let min = sequence.min_value.unwrap_or(1);
    let max = sequence.max_value.unwrap_or(i128::MAX);

    // The engine's add-sequence precheck rejects existing values in `min..max`.
    // The boundary probe intentionally places an existing value at `max`, so
    // only values below the exclusive upper bound make the migration invalid.
    domain.integral_values().all(|value| value < min || value >= max)
}

fn column_domain(model: &Model, table: &TablePlan, column: usize) -> Option<ColumnDomain> {
    let column_name = &table.columns.get(column)?.name;
    model.column_domain_by_name(&table.name, column_name)
}

fn removable_sequence_columns(table: &TablePlan) -> Vec<usize> {
    table.sequences.iter().map(|sequence| sequence.column).collect()
}

fn addable_unique_constraint_columns(table: &TablePlan) -> Vec<Vec<usize>> {
    (0..table.columns.len())
        .filter_map(|column| {
            let columns = vec![column];
            (!table
                .unique_constraints
                .iter()
                .any(|constraint| constraint.columns == columns))
            .then_some(columns)
        })
        .collect()
}

fn removable_unique_constraint_columns(table: &TablePlan) -> Vec<Vec<usize>> {
    table
        .unique_constraints
        .iter()
        .filter_map(|constraint| {
            (!table
                .primary_key
                .is_some_and(|primary_key| constraint.columns == [primary_key]))
            .then_some(constraint.columns.clone())
        })
        .collect()
}

fn has_index(table: &TablePlan, columns: &[usize]) -> bool {
    table.indexes.iter().any(|index| index.columns == columns)
}

fn is_required_index(table: &TablePlan, columns: &[usize]) -> bool {
    table.primary_key.is_some_and(|primary_key| columns == [primary_key])
        || table
            .unique_constraints
            .iter()
            .any(|constraint| constraint.columns == columns)
}

fn ensure_columns_exist(table: &TablePlan, columns: &[usize]) -> anyhow::Result<()> {
    anyhow::ensure!(!columns.is_empty(), "migration selected empty column list");
    anyhow::ensure!(
        columns.iter().all(|&column| column < table.columns.len()),
        "migration references missing column"
    );
    Ok(())
}

fn widen_sum_column(table: &mut TablePlan, column: Option<usize>) -> anyhow::Result<()> {
    let column = column.ok_or_else(|| anyhow::anyhow!("sum-widening migration missing column"))?;
    let Some(column_plan) = table.columns.get_mut(column) else {
        anyhow::bail!("sum-widening migration references missing column {column}");
    };

    let Type::Sum { variants } = &mut column_plan.ty else {
        anyhow::bail!("sum-widening migration selected a non-sum column");
    };
    anyhow::ensure!(
        *variants < MAX_SUM_VARIANTS,
        "sum-widening migration selected maxed-out sum column"
    );
    *variants += 1;
    Ok(())
}
